import math
import random

# Calculate the Square and Cube of a Random Number:
random_generated_number: int = random.randint(1,100)
print(f"Square of {random_generated_number} is: {math.sqrt(random_generated_number)}")
print(f"Cube of {random_generated_number} is: {math.pow(random_generated_number, 3)}")


